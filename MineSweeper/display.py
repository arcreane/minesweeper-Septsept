def display_map(map, width):
    cnt = 1
    output = """      """
    while cnt < width + 1:
        if cnt >= 10:
            output += f"""{cnt} """
        else:
            output += f""" {cnt} """
        cnt += 1

    output += "\n"
    cnt = 0
    char = 0

    while cnt < len(map):
        if cnt % width == 0:
            separator = 0
            output += """     """
            while separator < width:
                output += """═"""
                separator += 1
            output += f"""\n  {chr(char + 65)}  ║"""
            char += 1
        if map[cnt] == 0:
            output += f"""║█║"""
        else:
            output += f"""║ ║"""

        cnt += 1

    print(output)
