def display_map(map_mine, width):
    cnt = 1
    output = """      """
    while cnt < width + 1:
        if cnt >= 10:
            output += f"""{cnt} """
        else:
            output += f""" {cnt} """
        cnt += 1

    output += "\n"
    cnt = 0
    char = 1

    while cnt < len(map_mine):
        if cnt % width == 0:
            if char > 9:
                output += f"""\n  {char}  """
            else:
                output += f"""\n  {char}   """
            char += 1
        output += f""" {map_mine[cnt]} """

        cnt += 1

    print(output)


